class MemoryCompression(nn.Module):
    def __init__(self, d_model=512, compress_len=10, nhead=8, dropout=0.1):
        """
        Memory compression module using cross-attention mechanism.
        
        Args:
            d_model (int): Dimension of the model features
            compress_len (int): Target compressed length
            nhead (int): Number of attention heads
            dropout (float): Dropout probability
        """
        super().__init__()
        
        # Learnable query embeddings for compression
        self.query_embed = nn.Parameter(torch.randn(compress_len, d_model))
        
        # Multi-head cross attention
        self.multihead_attn = nn.MultiheadAttention(
            embed_dim=d_model,
            num_heads=nhead,
            dropout=dropout,
            batch_first=False
        )
        
        # Normalization layers
        self.norm1 = nn.LayerNorm(d_model)
        self.norm2 = nn.LayerNorm(d_model)
        
        # Feed-forward network for further processing
        self.linear1 = nn.Linear(d_model, d_model * 4)
        self.dropout = nn.Dropout(dropout)
        self.linear2 = nn.Linear(d_model * 4, d_model)
        
        # Activation function
        self.activation = nn.ReLU()
        
        self.dropout1 = nn.Dropout(dropout)
        self.dropout2 = nn.Dropout(dropout)
        
        self.d_model = d_model
        self.compress_len = compress_len
    
    def with_pos_embed(self, tensor, pos=None):
        """Add positional embeddings to the input if provided."""
        return tensor if pos is None else tensor + pos
    
    def forward(self, memory, mask=None, pos_embed=None):
        """
        Compress long-term memory features.
        
        Args:
            memory (Tensor): Long-term memory features of shape (L, batch_size, d_model)
            mask (Tensor, optional): Padding mask of shape (batch_size, L), True values are masked
            pos_embed (Tensor, optional): Positional embeddings of shape (L, batch_size, d_model)
            
        Returns:
            Tensor: Compressed memory features of shape (compress_len, batch_size, d_model)
        """
        # Get batch size
        _, batch_size, _ = memory.shape
        
        # Repeat query embeddings for each batch
        # Shape: (compress_len, batch_size, d_model)
        query = self.query_embed.unsqueeze(1).repeat(1, batch_size, 1)
        
        # Apply positional encoding to keys if provided
        if pos_embed is not None:
            memory = self.with_pos_embed(memory, pos_embed)
        
        # Convert mask for attention (if provided)
        # PyTorch's attention expects mask values of True to be masked
        attn_mask = None
        if mask is not None:
            # Ensure the mask has the correct dimensions for the attention module
            attn_mask = mask  # Shape: (batch_size, L)
        
        # Cross attention: queries attend to memory
        # query: (compress_len, batch_size, d_model)
        # memory: (L, batch_size, d_model)
        # Output shape: (compress_len, batch_size, d_model)
        attn_output, _ = self.multihead_attn(
            query=query,
            key=memory,
            value=memory,
            key_padding_mask=attn_mask
        )
        
        # Residual connection and normalization
        query = query + self.dropout1(attn_output)
        query = self.norm1(query)
        
        # Feed-forward network
        ff_output = self.linear2(self.dropout(self.activation(self.linear1(query))))
        
        # Residual connection and normalization
        compressed_features = query + self.dropout2(ff_output)
        compressed_features = self.norm2(compressed_features)
        
        # Shape: (compress_len, batch_size, d_model)
        return compressed_features

class Transformer(nn.Module):

    def __init__(self, d_model=512, nhead=8, num_encoder_layers=6,
                num_decoder_layers=6, dim_feedforward=2048, dropout=0.1,
                activation="relu", normalize_before=False,
                return_intermediate_dec=False, compress_len=10):
        super().__init__()

        # 原始Transformer编码器和解码器
        # TransformerEncoderLayerThin
        encoder_layer = TransformerEncoderLayer(d_model, nhead, dim_feedforward,
                                                dropout, activation, normalize_before)
        encoder_norm = nn.LayerNorm(d_model) if normalize_before else None
        self.encoder = TransformerEncoder(encoder_layer, num_encoder_layers, encoder_norm)

        # TransformerDecoderLayerThin
        decoder_layer = TransformerDecoderLayer(d_model, nhead, dim_feedforward,
                                                dropout, activation, normalize_before)
        decoder_norm = nn.LayerNorm(d_model)
        self.decoder = TransformerDecoder(decoder_layer, num_decoder_layers, decoder_norm,
                                          return_intermediate=return_intermediate_dec)

        # 添加压缩模块
        self.memory_compressor = MemoryCompression(
            d_model=d_model,
            compress_len=compress_len,
            nhead=nhead,
            dropout=dropout
        )

        self._reset_parameters()

        self.d_model = d_model
        self.nhead = nhead

    def _reset_parameters(self):
        for p in self.parameters():
            if p.dim() > 1:
                nn.init.xavier_uniform_(p)

    def forward(self, src, mask, query_embed, pos_embed, use_memory_compression=True):
        """
        Args:
            src: (batch_size, L, d)
            mask: (batch_size, L)
            query_embed: (#queries, d)
            pos_embed: (batch_size, L, d) the same as src
            use_compression: bool, whether to use compression
        Returns:

        """
        # 调整维度顺序
        # flatten NxCxHxW to HWxNxC
        bs, l, d = src.shape
        src = src.permute(1, 0, 2)  # (L, batch_size, d)
        pos_embed = pos_embed.permute(1, 0, 2)   # (L, batch_size, d)
        query_embed = query_embed.unsqueeze(1).repeat(1, bs, 1)  # (#queries, batch_size, d)
        tgt = torch.zeros_like(query_embed)
        # 编码器处理
        memory = self.encoder(src, src_key_padding_mask=mask, pos=pos_embed)  # (L, batch_size, d)
        if use_memory_compression:
            # 应用压缩模块
            compressed_memory = self.memory_compressor(memory, mask, pos_embed)  # (compress_len, batch_size, d)
            
            # 创建压缩后的掩码 - 压缩后所有位置都是有效的
            compressed_mask = torch.zeros((bs, compressed_memory.size(0)), 
                                           dtype=torch.bool, 
                                           device=mask.device)
            
            # 使用压缩后的记忆和掩码进行解码
            hs = self.decoder(tgt, compressed_memory, memory_key_padding_mask=compressed_mask,
                            pos=None, query_pos=query_embed)  # (#layers, #queries, batch_size, d)
            
            memory_output = compressed_memory.transpose(0, 1)  # (batch_size, compress_len, d)
        else:
            # 不使用压缩，直接进行解码
            hs = self.decoder(tgt, memory, memory_key_padding_mask=mask,
                            pos=pos_embed, query_pos=query_embed)  # (#layers, #queries, batch_size, d)
            memory_output = memory.transpose(0, 1)  # (batch_size, L, d)
        
        hs = hs.transpose(1, 2)  # (#layers, batch_size, #qeries, d)

        return hs, memory, memory_output


class TransformerEncoder(nn.Module):

    def __init__(self, encoder_layer, num_layers, norm=None, return_intermediate=False):
        super().__init__()
        self.layers = _get_clones(encoder_layer, num_layers)
        self.num_layers = num_layers
        self.norm = norm
        self.return_intermediate = return_intermediate

    def forward(self, src,
                mask: Optional[Tensor] = None,
                src_key_padding_mask: Optional[Tensor] = None,
                pos: Optional[Tensor] = None):
        output = src

        intermediate = []

        for layer in self.layers:
            output = layer(output, src_mask=mask,
                           src_key_padding_mask=src_key_padding_mask, pos=pos)
            if self.return_intermediate:
                intermediate.append(output)

        if self.norm is not None:
            output = self.norm(output)

        if self.return_intermediate:
            return torch.stack(intermediate)

        return output


class TransformerDecoder(nn.Module):

    def __init__(self, decoder_layer, num_layers, norm=None, return_intermediate=False):
        super().__init__()
        self.layers = _get_clones(decoder_layer, num_layers)
        self.num_layers = num_layers
        self.norm = norm
        self.return_intermediate = return_intermediate

    def forward(self, tgt, memory,
                tgt_mask: Optional[Tensor] = None,
                memory_mask: Optional[Tensor] = None,
                tgt_key_padding_mask: Optional[Tensor] = None,
                memory_key_padding_mask: Optional[Tensor] = None,
                pos: Optional[Tensor] = None,
                query_pos: Optional[Tensor] = None):
        output = tgt

        intermediate = []

        for layer in self.layers:
            output = layer(output, memory, tgt_mask=tgt_mask,
                           memory_mask=memory_mask,
                           tgt_key_padding_mask=tgt_key_padding_mask,
                           memory_key_padding_mask=memory_key_padding_mask,
                           pos=pos, query_pos=query_pos)
            if self.return_intermediate:
                intermediate.append(self.norm(output))

        if self.norm is not None:
            output = self.norm(output)
            if self.return_intermediate:
                intermediate.pop()
                intermediate.append(output)

        if self.return_intermediate:
            return torch.stack(intermediate)

        return output.unsqueeze(0)


